/*
 * 『こんにちは。』と表示して改行するメソッドhelloを作成せよ。
 */
package ensyu_7_5;

public class ensyu_7_5 {
	/*
	 * 関数名：hello
	 * 説明：『こんにちは。』と表示して改行するメソッド
	 * パラメータ：なし
	 * 返り値：なし
	 * 作成者：早田
	 * 作成日：2022/5/27
	 */
	static void hello() {
		// 出力
		System.out.println("こんにちは。");
		// 改行分の出力
		System.out.println();
	}
	public static void main(String[] args) {
		// メソッドの呼び出し
		hello();
	}
}
